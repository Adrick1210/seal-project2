// Edit Modal Functions
function showModal(id) {
  // console.log(id);
  const dialog = document.getElementById(id);
  dialog.showModal();
}

// Show Modal Function
function displayModal(id) {
  // console.log(id);
  const dialog = document.getElementById(id);
  dialog.showModal();
}
// Close Button Function
function closeModal(id) {
  const dialog = document.getElementById(id);
  dialog.close();
}
